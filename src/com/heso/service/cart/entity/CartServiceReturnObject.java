package com.heso.service.cart.entity;

import java.io.Serializable;
import java.util.ArrayList;

public class CartServiceReturnObject implements Serializable{

	public CartServiceReturnObject() {
		code = "000000";
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getAccount() {
		return account;
	}

	public void setAccount(String account) {
		this.account = account;
	}

	public String getCount() {
		return count;
	}

	public void setCount(String count) {
		this.count = count;
	}

	public ArrayList<CartItemObject> getCioList() {
		return cioList;
	}

	public void setCioList(ArrayList<CartItemObject> cioList) {
		this.cioList = cioList;
	}

	private String code;
	private String account;
	private String count;
	private ArrayList<CartItemObject> cioList;
}
