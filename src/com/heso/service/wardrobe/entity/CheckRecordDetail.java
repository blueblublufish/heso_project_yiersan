package com.heso.service.wardrobe.entity;

public class CheckRecordDetail {
	
	private String id;
	private String recordId;
	private String wardrobeOrderId;
	private String image;
	private String name;
	private String creatime;
	private String recordType;
	private String account;
	private String price;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getRecordId() {
		return recordId;
	}
	public void setRecordId(String recordId) {
		this.recordId = recordId;
	}
	public String getWardrobeOrderId() {
		return wardrobeOrderId;
	}
	public void setWardrobeOrderId(String wardrobeOrderId) {
		this.wardrobeOrderId = wardrobeOrderId;
	}
	public String getImage() {
		return image;
	}
	public void setImage(String image) {
		this.image = image;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getCreatime() {
		return creatime;
	}
	public void setCreatime(String creatime) {
		this.creatime = creatime;
	}
	public String getRecordType() {
		return recordType;
	}
	public void setRecordType(String recordType) {
		this.recordType = recordType;
	}
	public String getAccount() {
		return account;
	}
	public void setAccount(String account) {
		this.account = account;
	}
	public String getPrice() {
		return price;
	}
	public void setPrice(String price) {
		this.price = price;
	}
	
	
}
